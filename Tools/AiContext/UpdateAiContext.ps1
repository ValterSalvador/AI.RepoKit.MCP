        arrayDefaultLimit = 25
        arrayHardLimit = 100
        previewChars = 240
    }
}

New-Item -ItemType Directory -Path $manifestRoot -Force | Out-Null
New-Item -ItemType Directory -Path $inventoriesRoot -Force | Out-Null
New-Item -ItemType Directory -Path $summariesRoot -Force | Out-Null
($summary | ConvertTo-Json -Depth 8) | Set-Content -LiteralPath $manifestPath -Encoding UTF8
([ordered]@{ generatedAtLocal = $summary.generatedAtLocal; projects = @($projectItems) } | ConvertTo-Json -Depth 8) | Set-Content -LiteralPath (Join-Path $inventoriesRoot "project-inventory.json") -Encoding UTF8
([ordered]@{ generatedAtLocal = $summary.generatedAtLocal; references = @($references) } | ConvertTo-Json -Depth 8) | Set-Content -LiteralPath (Join-Path $inventoriesRoot "project-references.json") -Encoding UTF8
([ordered]@{ generatedAtLocal = $summary.generatedAtLocal; packages = @($packages) } | ConvertTo-Json -Depth 8) | Set-Content -LiteralPath (Join-Path $inventoriesRoot "package-inventory.json") -Encoding UTF8
([ordered]@{ generatedAtLocal = $summary.generatedAtLocal; dotNetSdkVersion = $sdkVersion; dotNetSdks = @($sdkList); expectedTargetFramework = "net10.0" } | ConvertTo-Json -Depth 8) | Set-Content -LiteralPath (Join-Path $inventoriesRoot "sdk-inventory.json") -Encoding UTF8
$summaryMarkdown = @(
    "# Generated Context Summary",
    "",
    "- Generated: $($summary.generatedAtLocal)",
    "- Main solution: $($summary.mainSolution)",
    "- Projects: $($summary.projectCount)",
    "- Packages: $(@($packages).Count)",
    "- Project references: $(@($references).Count)"
)
$summaryMarkdown | Set-Content -LiteralPath (Join-Path $summariesRoot "generated-context-summary.md") -Encoding UTF8
Write-Output (Redact "Updated .ai/generated/ context inventories and summaries.")
